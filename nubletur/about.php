				<h2>Hidrografía</h2>
				<p>La región del Ñuble se asocia principalmente al gran sistema fluvial del Río Itata, dren principal que nace en la Cordillera de Los Andes, y que en su recorrido recibe los desagües de buena parte de las cuencas altas de la división administrativa.</p>
				<p>La hoya hidrográfica del río Itata alcanza una superficie de 11.293 km2 y está formada principalmente por tres cursos de agua: los ríos Itata, Ñuble y Diguillín. Posee una longitud de 180 km incluyendo dos ríos formativos: el Cholguán y Huépil. Su régimen es mixto, ya que los principales afluentes nacen en la Cordillera y Precordillera de Los Andes adquiriendo su aporte de los deshielos primaverales, además del aporte hecho por las precipitaciones invernales. Este río cruza el valle longitudinal donde en su entrada presenta un gran salto de agua que muestra el desnivel del terreno. Su caudal medio anual varía entre 30 y 67 m3, según sea el año estudiado y la estación fluviométrica utilizada.</p>
				<p>Respecto del Río Ñuble, es el principal dren en el transporte de las aguas de las cuencas cordilleranas de la sección norte de la región. Subtendiendo una superficie de 4.774 km2, es el dren que recoge los aportes de la sección norte de la región, incluyendo a sus tributarios: Río Chillán, Río Cato y Río Changaral.</p>
				<p>El Río Diguillín por su parte es el principal curso que drena la sección latitudinal centro-sur de la región. Con una cuenca de 1.386 km2, este río va colectando los aportes de los cursos cordilleranos, como el Estero Renegado, para incorporar incluso aguas provenientes de otras cuencas mediante el canal Laja-Diguillín, y que ha provisto de un suplemento de aguas destinadas al regadío a la zona.</p>
