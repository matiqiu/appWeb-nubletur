				<h2>Región de Ñuble</h2>
				<p>La Región del Ñuble se localiza cercana al límite sur de la zona central, específicamente entre los 36º00' y los 37º12' de latitud sur. Limita al norte con la Región del Maule, al sur con la Región del Biobío, al oeste con el Océano Pacífico y al este con la República Argentina. Consta con una superficie aproximada de 13.178 km2 representando a la región con menor extensión del país. El paisaje regional presenta las unidades longitudinales clásicas de la zona central de Chile, con presencia de algunos elementos propios como lo es la presencia de una precordillera, de gran relevancia en la configuración espacial de Ñuble. Según el Censo 2017 la población alcanza los 480.609 habitantes y una densidad de 36,47 habitantes por kilómetro cuadrado.</p>
				<p>Respecto a las condiciones climáticas, esta zona se podría definir como el último tramo de la región mediterránea de estación seca prolongada del Chile Central. Estas condiciones le confieren un carácter con algunas variaciones extremas según la estación del año. La red hidrográfica de la región se organiza en torno al principal sistema fluvial: el Río Itata. A partir de este drenaje se articulan el resto de las subcuencas y sistemas de escurrimiento menores.</p>
				<p>Ñuble a pesar de ser una región nueva dentro de la división político-administrativa del país, es una zona de mucha tradición, la cual se vincula con la imagen más representativa del campo chileno, y en general con el Valle Central de nuestro país. Su sistema urbano posee uno de los ejemplos de conurbación que existen en Chile: Chillán-Chillán Viejo. Su economía se basa en rasgos variados vinculados a la actividad agraria, pecuaria, silvícola, el comercio y los servicios personales.</p>
				<p>Además de ello, Ñuble se caracteriza por ser la cuna de grandes personajes, de gran trascendencia en la historia de Chile, como Bernardo O'Higgins y Arturo Prat; o grandes figuras de la esfera cultural como Claudio Arrau, Violeta Parra y Marta Brunet, entre otros.</p>
